Object.defineProperty(Object.prototype, 'write', {
    value: function (val) {
        for (var temp in val) {
            this[temp] = val[temp];
        }
        return this;
    }
});
/*直接寫入*/

Object.defineProperty(Object.prototype, 'modify', {
    value: function (val) {
        for (var temp in val) {
            if (this[temp] != undefined) {
                this[temp] = val[temp];
            }
        }
        return this;
    }
});
/*物件有存在的才直接寫入*/


var numberCrop = function (pValue, pMin, pMax) {
    if (pValue < pMin) {
        return pMin;
    }
    if (pValue > pMax) {
        return pMax;
    }
    return pValue
}
